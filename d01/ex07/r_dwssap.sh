#!/bin/sh

cat /etc/passwd | grep -v "#" | sed -n "n;p" | cut -d ':' -f 1 | rev | sort -rf | sed -n "$FT_LINE1, $FT_LINE2 p" | cat -e | tr '$' ',' | tr '\n' ' ' | rev | cut -c 3- | rev | sed 's/$/./'

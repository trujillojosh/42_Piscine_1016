int		nmatch(char *s1, char *s2)
{
	int	i;
	int	c;
	int	combo;

	i = 0;
	c = 0;
	combo = 0;
	if (s1[i] == '*' && s2[c] == '*')
		i++;
	if (s1[i] == s2[c])
	{
		i++;
		c++;
	}
	if (*s1 == '\0' && *s2 == '\0')
		return (1);
	if (s1[i] != s2[c] && s2[c] != '*')
		return (0);
	if (s2[c] == '*' && s2[c + 1] != s1[i] && s2[c + 2] != '\0')
		c++;
	if (s1[i] == '\0' && s2[c] != '\0' && s2[c] != '*')
		return (0);
	if (s2[c + 1] == '\0' && s2[c] == '*')
		return (1);
	return (match(s1 + 1, s2 + 1) + match(s1 + 1, s2));
}

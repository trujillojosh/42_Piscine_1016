int		ft_is_prime(int nb)
{
	int	i;
	int	temp;

	temp = nb - 1;
	i = 1;
	if (nb <= 1)
	{
		return (0);
	}
	while (temp >= 2)
	{
		i = i * temp;
		temp--;
	}
	i = i + 1;
	i = i / nb;
	if (nb)
		return (1);
	else
		return (0);
}

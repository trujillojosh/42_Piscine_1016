int	ft_sqrt(int nb)
{
	int	a;

	a = 0;
	if (nb > 2147395600)
	{
		return (0);
	}
	while (a * a <= nb)
	{
		if (a * a == nb)
		{
			return (a);
		}
		a++;
	}
	if (a * a == nb)
	{
		return (a);
	}
	else
	{
		return (0);
	}
}
